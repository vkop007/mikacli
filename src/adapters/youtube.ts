  private buildNetscapeCookies(session: PlatformSession): string {
    const header = ["# Netscape HTTP Cookie File", "# This file was generated by AutoCLI.", ""];
    const cookies = Array.isArray(session.cookieJar.cookies) ? session.cookieJar.cookies : [];

    const lines = cookies
      .filter(
        (cookie): cookie is {
          key: string;
          value: string;
          domain?: string;
          path?: string;
          secure?: boolean;
          expires?: string | number | Date;
          hostOnly?: boolean;
        } => Boolean(cookie && typeof cookie.key === "string" && typeof cookie.value === "string"),
      )
      .map((cookie) => {
        const domain = cookie.domain ?? "youtube.com";
        const includeSubdomains = domain.startsWith(".") ? "TRUE" : "FALSE";
        const path = cookie.path ?? "/";
        const secure = cookie.secure ? "TRUE" : "FALSE";
        const expires = this.toNetscapeExpiry(cookie.expires);
        return [domain, includeSubdomains, path, secure, String(expires), cookie.key, cookie.value].join("\t");
      });

    return [...header, ...lines, ""].join("\n");
  }

  private toNetscapeExpiry(value: unknown): number {
    if (typeof value === "number" && Number.isFinite(value)) {
      return value > 10_000_000_000 ? Math.floor(value / 1_000) : Math.floor(value);
    }

    if (typeof value === "string") {
      if (!value || value === "Infinity") {
        return 0;
      }

      const asNumber = Number(value);
      if (!Number.isNaN(asNumber)) {
        return asNumber > 10_000_000_000 ? Math.floor(asNumber / 1_000) : Math.floor(asNumber);
      }

      const parsed = Date.parse(value);
      return Number.isNaN(parsed) ? 0 : Math.floor(parsed / 1_000);
    }

    if (value instanceof Date) {
      return Math.floor(value.getTime() / 1_000);
    }

    return 0;
  }

  private async requireCommand(
    command: string,
    args: string[],
    errorCode: string,
    errorMessage: string,
  ): Promise<void> {
    try {
      await this.runProcess(command, args);
    } catch (error) {
      if (
        error instanceof Error &&
        "cause" in error &&
        error.cause &&
        typeof error.cause === "object" &&
        "code" in error.cause &&
        error.cause.code === "ENOENT"
      ) {
        throw new AutoCliError(errorCode, errorMessage, {
          details: {
            command,
          },
          cause: error,
        });
      }

      throw error;
    }
  }

  private async runProcess(
    command: string,
    args: string[],
  ): Promise<{ stdout: string; stderr: string }> {
    return new Promise((resolvePromise, rejectPromise) => {
      const child = spawn(command, args, {
        stdio: ["ignore", "pipe", "pipe"],
      });

      let stdout = "";
      let stderr = "";

      child.stdout.on("data", (chunk) => {
        stdout += String(chunk);
      });

      child.stderr.on("data", (chunk) => {
        stderr += String(chunk);
      });

      child.on("error", (error) => {
        rejectPromise(new Error(`Failed to start ${command}.`, { cause: error }));
      });

      child.on("close", (code) => {
        if (code === 0) {
          resolvePromise({ stdout, stderr });
          return;
        }

        rejectPromise(
          new AutoCliError("PROCESS_FAILED", `${command} exited with code ${code ?? "unknown"}.`, {
            details: {
              command,
              args,
              code: code ?? undefined,
              stderr: stderr.trim() || undefined,
            },
          }),
        );
      });
    });
  }

  private extractDownloadedPath(stdout: string): string | undefined {
    const lines = stdout
      .split(/\r?\n/u)
      .map((line) => line.trim())
      .filter(Boolean);

    return lines.at(-1);
  }

