  async download(input: {
    account?: string;
    target: string;
    outputDir?: string;
    filenameTemplate?: string;
    audioOnly?: boolean;
    audioFormat?: string;
    format?: string;
  }): Promise<AdapterActionResult> {
    const { session } = await this.prepareSession(input.account);
    await this.ensureUsableSession(session);

    const target = parseYouTubeTarget(input.target);
    const context = await this.prepareVideoActionContext(session, target.videoId, target.url);
    const outputDir = resolve(input.outputDir ?? join(process.cwd(), "downloads", "youtube"));
    const filenameTemplate = input.filenameTemplate?.trim() || "%(title)s [%(id)s].%(ext)s";
    const audioOnly = Boolean(input.audioOnly);
    const audioFormat = (input.audioFormat?.trim() || "mp3").toLowerCase();
    const format = input.format?.trim() || (audioOnly ? "bestaudio/best" : "bestvideo*+bestaudio/best");

    await this.requireCommand("yt-dlp", ["--version"], "YTDLP_NOT_FOUND", "yt-dlp is required for youtube download.");
    await this.requireCommand("ffmpeg", ["-version"], "FFMPEG_NOT_FOUND", "ffmpeg is required for youtube download.");
    await mkdir(outputDir, { recursive: true });

    const tempDir = await mkdtemp(join(tmpdir(), "autocli-youtube-download-"));
    const cookiesPath = join(tempDir, "cookies.txt");

    try {
      await writeFile(cookiesPath, this.buildNetscapeCookies(session), "utf8");

      const args = [
        "--cookies",
        cookiesPath,
        "--no-simulate",
        "--no-progress",
        "--no-warnings",
        "--print",
        "after_move:filepath",
        "--output",
        join(outputDir, filenameTemplate),
        "--format",
        format,
      ];

      if (audioOnly) {
        args.push("--extract-audio", "--audio-format", audioFormat);
      }

      args.push(context.url);

      const { stdout, stderr } = await this.runProcess("yt-dlp", args);
      const outputPath = this.extractDownloadedPath(stdout);

      return {
        ok: true,
        platform: this.platform,
        account: session.account,
        action: "download",
        message: `YouTube download completed for ${session.account}.`,
        id: target.videoId,
        url: context.url,
        data: {
          outputPath,
          outputDir,
          audioOnly,
          audioFormat: audioOnly ? audioFormat : undefined,
          format,
          stderr: stderr || undefined,
        },
      };
    } catch (error) {
      if (isAutoCliError(error)) {
        throw error;
      }

      throw new AutoCliError("YOUTUBE_DOWNLOAD_FAILED", "Failed to download the YouTube video.", {
        cause: error,
        details: error instanceof Error ? { message: error.message } : undefined,
      });
    } finally {
      await rm(tempDir, { recursive: true, force: true }).catch(() => undefined);
    }
  }

