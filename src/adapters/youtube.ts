  private matchQuotedValue(html: string, pattern: RegExp): string | undefined {
    const match = html.match(pattern);
    if (!match?.[1]) {
      return undefined;
    }

    try {
      return JSON.parse(`"${match[1]}"`) as string;
    } catch {
      return match[1];
    }
  }

  private matchBoolean(html: string, pattern: RegExp): boolean | undefined {
    const match = html.match(pattern);
    if (!match?.[1]) {
      return undefined;
    }

    return match[1] === "true";
  }

