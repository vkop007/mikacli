  private extractRelatedResults(
    response: Record<string, unknown>,
    targetVideoId: string,
    limit: number,
  ): YouTubeSearchResultItem[] {
    const results: YouTubeSearchResultItem[] = [];
    this.walkForRelatedResults(response, results, limit, targetVideoId);
    return results;
  }

  private walkForSearchResults(node: unknown, results: YouTubeSearchResultItem[], limit: number): void {
    if (results.length >= limit || node == null) {
      return;
    }

    if (Array.isArray(node)) {
      for (const entry of node) {
        this.walkForSearchResults(entry, results, limit);
        if (results.length >= limit) {
          return;
        }
      }
      return;
    }

    if (typeof node !== "object") {
      return;
    }

    if ("videoRenderer" in node) {
      const item = this.parseVideoSearchRenderer((node as { videoRenderer?: unknown }).videoRenderer);
      if (item) {
        results.push(item);
      }
      return;
    }

    for (const value of Object.values(node)) {
      this.walkForSearchResults(value, results, limit);
      if (results.length >= limit) {
        return;
      }
    }
  }

  private walkForRelatedResults(
    node: unknown,
    results: YouTubeSearchResultItem[],
    limit: number,
    targetVideoId: string,
  ): void {
    if (results.length >= limit || node == null) {
      return;
    }

    if (Array.isArray(node)) {
      for (const entry of node) {
        this.walkForRelatedResults(entry, results, limit, targetVideoId);
        if (results.length >= limit) {
          return;
        }
      }
      return;
    }

    if (typeof node !== "object") {
      return;
    }

    if ("compactVideoRenderer" in node) {
      const item = this.parseCompactVideoRenderer(
        (node as { compactVideoRenderer?: unknown }).compactVideoRenderer,
        targetVideoId,
      );
      if (item) {
        results.push(item);
      }
      return;
    }

    if ("lockupViewModel" in node) {
      const item = this.parseLockupViewModel((node as { lockupViewModel?: unknown }).lockupViewModel, targetVideoId);
      if (item) {
        results.push(item);
      }
      return;
    }

    for (const value of Object.values(node)) {
      this.walkForRelatedResults(value, results, limit, targetVideoId);
      if (results.length >= limit) {
        return;
      }
    }
  }

  private parseVideoSearchRenderer(renderer: unknown): YouTubeSearchResultItem | undefined {
    if (!renderer || typeof renderer !== "object" || !("videoId" in renderer) || typeof renderer.videoId !== "string") {
      return undefined;
    }

    const title = this.extractTextValue("title" in renderer ? renderer.title : undefined);
    if (!title) {
      return undefined;
    }

    const videoId = renderer.videoId;
    return {
      id: videoId,
      title,
      url: `${YOUTUBE_WATCH}${videoId}`,
      channel: this.extractTextValue(
        ("longBylineText" in renderer ? renderer.longBylineText : undefined) ??
          ("ownerText" in renderer ? renderer.ownerText : undefined),
      ),
      duration: this.extractTextValue("lengthText" in renderer ? renderer.lengthText : undefined),
      views: this.extractTextValue("viewCountText" in renderer ? renderer.viewCountText : undefined),
      published: this.extractTextValue("publishedTimeText" in renderer ? renderer.publishedTimeText : undefined),
      description: this.extractDescriptionValue(renderer),
      thumbnailUrl: this.extractThumbnailUrl("thumbnail" in renderer ? renderer.thumbnail : undefined),
    };
  }

  private parseCompactVideoRenderer(renderer: unknown, targetVideoId: string): YouTubeSearchResultItem | undefined {
    if (!renderer || typeof renderer !== "object" || !("videoId" in renderer) || typeof renderer.videoId !== "string") {
      return undefined;
    }

    if (renderer.videoId === targetVideoId) {
      return undefined;
    }

    const title = this.extractTextValue("title" in renderer ? renderer.title : undefined);
    if (!title) {
      return undefined;
    }

    return {
      id: renderer.videoId,
      title,
      url: `${YOUTUBE_WATCH}${renderer.videoId}`,
      channel: this.extractTextValue(
        ("longBylineText" in renderer ? renderer.longBylineText : undefined) ??
          ("shortBylineText" in renderer ? renderer.shortBylineText : undefined),
      ),
      duration: this.extractTextValue("lengthText" in renderer ? renderer.lengthText : undefined),
      views:
        this.extractTextValue("viewCountText" in renderer ? renderer.viewCountText : undefined) ??
        this.extractTextValue("shortViewCountText" in renderer ? renderer.shortViewCountText : undefined),
      published: this.extractTextValue("publishedTimeText" in renderer ? renderer.publishedTimeText : undefined),
      description: this.extractDescriptionValue(renderer as Record<string, unknown>),
      thumbnailUrl: this.extractThumbnailUrl("thumbnail" in renderer ? renderer.thumbnail : undefined),
    };
  }

  private parseLockupViewModel(renderer: unknown, targetVideoId: string): YouTubeSearchResultItem | undefined {
    if (
      !renderer ||
      typeof renderer !== "object" ||
      !("contentId" in renderer) ||
      typeof renderer.contentId !== "string" ||
      renderer.contentId === targetVideoId
    ) {
      return undefined;
    }

    const title = this.extractTextValueAtPath(renderer, ["metadata", "lockupMetadataViewModel", "title"]);
    if (!title) {
      return undefined;
    }

    const metadataRows = this.getNestedValue(renderer, [
      "metadata",
      "lockupMetadataViewModel",
      "metadata",
      "contentMetadataViewModel",
      "metadataRows",
    ]);
    const rowZeroParts = this.extractMetadataRowTexts(metadataRows, 0);
    const rowOneParts = this.extractMetadataRowTexts(metadataRows, 1);

    return {
      id: renderer.contentId,
      title,
      url: `${YOUTUBE_WATCH}${renderer.contentId}`,
      channel: rowZeroParts[0],
      views: rowOneParts[0],
      published: rowOneParts[1],
      duration: this.extractDurationFromLockup(renderer),
      thumbnailUrl: this.extractNestedThumbnailUrl("contentImage" in renderer ? renderer.contentImage : undefined),
    };
  }

