    error.code === "YOUTUBE_UPLOAD_THUMBNAIL_MISSING" ||
    error.code === "YOUTUBE_UPLOAD_VISIBILITY_INVALID" ||
    error.code === "YOUTUBE_POST_IMAGE_MISSING"
