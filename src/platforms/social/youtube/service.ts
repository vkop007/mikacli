  private extractMetadataRowTexts(metadataRows: unknown, rowIndex: number): string[] {
    if (!Array.isArray(metadataRows) || !metadataRows[rowIndex] || typeof metadataRows[rowIndex] !== "object") {
      return [];
    }

    const row = metadataRows[rowIndex];
    const metadataParts =
      "metadataParts" in row && Array.isArray(row.metadataParts) ? row.metadataParts : [];

    return metadataParts
      .map((part: unknown) =>
        part && typeof part === "object" && "text" in part ? this.extractTextValue(part.text) : undefined,
      )
      .filter((value: string | undefined): value is string => typeof value === "string" && value.length > 0);
  }

  private extractTextValue(node: unknown): string | undefined {
    if (!node || typeof node !== "object") {
      return typeof node === "string" ? node : undefined;
    }

    if ("content" in node && typeof node.content === "string") {
      return node.content;
    }

    if ("simpleText" in node && typeof node.simpleText === "string") {
      return node.simpleText;
    }

    if ("runs" in node && Array.isArray(node.runs)) {
      const text = node.runs
        .map((entry) =>
          entry && typeof entry === "object" && "text" in entry && typeof entry.text === "string" ? entry.text : "",
        )
        .filter(Boolean)
        .join("");
      return text || undefined;
    }

    return undefined;
  }

  private extractTextValueAtPath(node: unknown, path: string[]): string | undefined {
    const target = this.getNestedValue(node, path);
    return this.extractTextValue(target);
  }

  private extractCaptionTracks(response: Record<string, unknown>): YouTubeCaptionTrack[] {
    const captions =
      "captions" in response && response.captions && typeof response.captions === "object" ? response.captions : undefined;
    const trackListRenderer =
      captions &&
      "playerCaptionsTracklistRenderer" in captions &&
      captions.playerCaptionsTracklistRenderer &&
      typeof captions.playerCaptionsTracklistRenderer === "object"
        ? captions.playerCaptionsTracklistRenderer
        : undefined;
    const captionTracks =
      trackListRenderer &&
      "captionTracks" in trackListRenderer &&
      Array.isArray(trackListRenderer.captionTracks)
        ? trackListRenderer.captionTracks
        : [];

    const tracks: YouTubeCaptionTrack[] = [];

    for (const track of captionTracks) {
      if (!track || typeof track !== "object") {
        continue;
      }

      const language =
        this.extractTextValue("name" in track ? track.name : undefined) ??
        ("languageCode" in track && typeof track.languageCode === "string" ? track.languageCode : undefined);
      const languageCode = "languageCode" in track && typeof track.languageCode === "string" ? track.languageCode : undefined;

      if (!language || !languageCode) {
        continue;
      }

      tracks.push({
        language,
        languageCode,
        kind: "kind" in track && typeof track.kind === "string" ? track.kind : undefined,
        autoGenerated: ("kind" in track && track.kind === "asr") || /\(auto-generated\)$/i.test(language),
        isTranslatable:
          "isTranslatable" in track && typeof track.isTranslatable === "boolean" ? track.isTranslatable : undefined,
      });
    }

    return tracks;
  }

  private extractAssignedJsonObject(html: string, variableName: string): Record<string, unknown> {
    const patterns = [`var ${variableName} = `, `${variableName} = `];
    for (const marker of patterns) {
      const markerIndex = html.indexOf(marker);
      if (markerIndex === -1) {
        continue;
      }

      const objectStart = html.indexOf("{", markerIndex + marker.length);
      if (objectStart === -1) {
        continue;
      }

      const objectText = this.readBalancedJsonObject(html, objectStart);
      if (!objectText) {
        continue;
      }

      return JSON.parse(objectText) as Record<string, unknown>;
    }

    throw new AutoCliError("YOUTUBE_PAGE_CONFIG_MISSING", `Missing ${variableName} from the YouTube page.`);
  }

  private readBalancedJsonObject(source: string, startIndex: number): string | undefined {
    let depth = 0;
    let inString = false;
    let escaped = false;

    for (let index = startIndex; index < source.length; index += 1) {
      const character = source[index];

      if (inString) {
        if (escaped) {
          escaped = false;
          continue;
        }

        if (character === "\\") {
          escaped = true;
          continue;
        }

        if (character === "\"") {
          inString = false;
        }

        continue;
      }

      if (character === "\"") {
        inString = true;
        continue;
      }

      if (character === "{") {
        depth += 1;
      } else if (character === "}") {
        depth -= 1;
        if (depth === 0) {
          return source.slice(startIndex, index + 1);
        }
      }
    }

    return undefined;
  }

  private findRenderer(node: unknown, rendererKey: string): Record<string, unknown> | undefined {
    if (!node || typeof node !== "object") {
      return undefined;
    }

    if (rendererKey in node) {
      const value = node[rendererKey as keyof typeof node];
      return value && typeof value === "object" ? (value as Record<string, unknown>) : undefined;
    }

    if (Array.isArray(node)) {
      for (const entry of node) {
        const nested = this.findRenderer(entry, rendererKey);
        if (nested) {
          return nested;
        }
      }
      return undefined;
    }

    for (const value of Object.values(node)) {
      const nested = this.findRenderer(value, rendererKey);
      if (nested) {
        return nested;
      }
    }

    return undefined;
  }

  private extractHandleFromUrl(url?: string): string | undefined {
    if (!url) {
      return undefined;
    }

    const match = url.match(/\/(@[A-Za-z0-9._-]+)/);
    return match?.[1];
  }

  private mapYouTubeWriteError(error: unknown, fallbackMessage: string): AutoCliError {
    if (
      isAutoCliError(error) &&
      (error.code === "YOUTUBE_REQUEST_REJECTED" ||
        error.code === "YOUTUBE_VIDEO_UNAVAILABLE" ||
        error.code === "YOUTUBE_VIDEO_INFO_MISSING")
    ) {
      return error;
    }

    if (isAutoCliError(error) && error.code === "HTTP_REQUEST_FAILED") {
      const status = typeof error.details?.status === "number" ? error.details.status : undefined;

      if (status === 400) {
        return new AutoCliError(
          "YOUTUBE_REQUEST_REJECTED",
          "YouTube rejected this action request. The video may not allow this action, or the saved cookies need a fresh export.",
          {
            cause: error,
            details: error.details,
          },
        );
      }

      if (status === 401 || status === 403) {
        return new AutoCliError(
          "SESSION_EXPIRED",
          "YouTube rejected the saved session for this action. Re-export cookies from an active browser session.",
          {
            cause: error,
            details: error.details,
          },
        );
      }
    }

    return new AutoCliError("PLATFORM_REQUEST_FAILED", fallbackMessage, {
      cause: error,
      details:
        isAutoCliError(error) && error.details
          ? error.details
          : error instanceof Error
            ? { message: error.message }
            : undefined,
    });
  }

  private async persistSessionState(session: PlatformSession, probe: YouTubeProbe): Promise<void> {
    await this.persistExistingSession(session, {
      user: session.user,
      status: probe.status,
      metadata: {
        ...(session.metadata ?? {}),
        ...(probe.metadata ?? {}),
      },
    });
  }

  private async filterYouTubeCookies(sourceJar: CookieJar): Promise<CookieJar> {
    const filteredJar = new CookieJar();
    const cookies = await sourceJar.getCookies(YOUTUBE_HOME);

    for (const cookie of cookies) {
      if (!YOUTUBE_COOKIE_ALLOWLIST.has(cookie.key)) {
        continue;
      }

      const normalized = Cookie.fromJSON(cookie.toJSON());
      if (!normalized) {
        continue;
      }

      await filteredJar.setCookie(normalized, `https://${cookie.domain}${cookie.path || "/"}`, {
        ignoreError: true,
      });
    }

    return filteredJar;
  }
