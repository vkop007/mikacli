function extractBareYouTubeCommunityPostId(value: string | undefined): string | undefined {
  if (!value) {
    return undefined;
  }

  const trimmed = value.trim();
  if (!trimmed || /[/?#=&]/u.test(trimmed)) {
    return undefined;
  }

  return /^[A-Za-z0-9_-]{10,}$/u.test(trimmed) ? trimmed : undefined;
}

